# Ruby program to show the use of keywords

#!/usr/bin/ruby

# define class vehicle using 'class' keyword

class MyClass

    # define method using 'def' keyword
    def class_print
        puts "Printing from class method"

    # end method with 'end' keyword
    end

# also end class definition with 'end' keyword
end

# create object
obj = MyClass.new

obj.class_print